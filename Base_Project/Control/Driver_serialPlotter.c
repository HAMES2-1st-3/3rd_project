/*---------------------------------------------Function Implementations----------------------------------------------*/
void SP_install_var(char *name, char *format_specifier, void* p_var, uint8 var_size) {
    uint32 name_length = strlen(name);
    uint32 format_specifier_length = strlen(format_specifier);
    if(name_length + format_specifier_length > VAR_MONITORING_STRING_SIZE) return;

    var_list[var_list_cnt].format_string[0] = '\0';
    strncat(var_list[var_list_cnt].format_string, name, strlen(name));
    strncat(var_list[var_list_cnt].format_string, ":", strlen(":"));
    strncat(var_list[var_list_cnt].format_string, format_specifier, strlen(format_specifier));

    var_list[var_list_cnt].p_var = p_var;

    var_list[var_list_cnt].var_size = var_size;
    var_list_cnt++;
}

void plotting_variable() {
    for(int i = 0 ; i < var_list_cnt; i++) {
        USB_printf(var_list[i].format_string, var_list[i].p_var);
        if(i == var_list_cnt)
            USB_printf("\n");
        else
            USB_printf(", ");
    }
}



void USB_printf(pchar format,...)
{
    char      message[USB_UART_MAX_PRINT_SIZE + 1];
    Ifx_SizeT count;
    va_list   args;
    va_start(args, format);
    vsprintf((char *)message, format, args);
    va_end(args);
    count = (Ifx_SizeT)strlen(message);
    IFX_ASSERT(IFX_VERBOSE_LEVEL_ERROR, count < STDIF_DPIPE_MAX_PRINT_SIZE);
    USB_print_string(message, USB_UART_MAX_PRINT_SIZE);
    //return

}

void USB_print_string(char *str, uint32 max_len) {
    uint32 count = 0;

    char ch = *str;
    while(ch != 0 && count < max_len) {
        if(ch == '\n') {
            asclin3_uart_tx('\r');
            asclin3_uart_tx('\n');
        } else {
            asclin3_uart_tx(ch);
        }
        str++;
        max_len++;
        ch = *str;
    }
}


