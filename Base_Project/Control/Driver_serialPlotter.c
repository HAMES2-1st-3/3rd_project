static struct {
        char format_string[VAR_MONITORING_STRING_SIZE + 1];
        void *p_var;
        uint8 var_size;
}var_list[10];
static uint8 var_list_cnt = 0;


/*********************************************************************************************************************/

/*********************************************************************************************************************/
